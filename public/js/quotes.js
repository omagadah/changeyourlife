  // Citation du jour : déterministe selon le jour de l'année (chaque jour diffère).
  window.CYL_quoteOfDay = function () {
    const q = window.CYL_QUOTES;
    const now = new Date();
    const start = new Date(now.getFullYear(), 0, 0);
    const day = Math.floor((now - start) / 86400000); // 1..366
    // + décalage annuel : chaque jour diffère, et l'année suivante varie aussi.
    return q[((day - 1) + now.getFullYear() * 7) % q.length];
  };
